# ---------------------------------------------------------------------------
# 5. Feature importance
# ---------------------------------------------------------------------------
@st.cache_data(ttl=600, show_spinner=False)
def load_feature_importance(run_id: str = "latest") -> pd.DataFrame:
    """Top feature importances for a given run (or latest deployed)."""
    if run_id == "latest":
        runs = load_model_runs(limit=1)
        if not runs.empty:
            run_id = str(runs.iloc[0].get("run_id", ""))

    rows = _fetch("feature_importance", filters={"run_id": run_id},
                  order_by="-importance_score", limit=30)
    if rows:
        return pd.DataFrame(rows)

    # Local JSON fallback
    fi_path = _OUTPUTS / f"feature_importance_{run_id}.json"
    if fi_path.exists():
        try:
            data = json.loads(fi_path.read_text())
            return pd.DataFrame(data)
        except Exception:
            pass
    return pd.DataFrame(columns=["feature_name","importance_score","importance_rank"])



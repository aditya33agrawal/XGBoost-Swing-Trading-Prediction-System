"""Data loading layer for the Streamlit dashboard.

Each function:
  1. Tries Supabase first (via st.secrets or os.getenv)
  2. Falls back to local JSON / CSV files in outputs/

This means the app works in three modes:
  - Full Supabase: live data from cloud DB
  - JSON-only: reads outputs/ files generated by the pipeline
  - Empty: returns empty DataFrames with correct schema
"""
from __future__ import annotations

import json
import os
from dataclasses import asdict
from pathlib import Path

import pandas as pd
import streamlit as st

from app.utils.env import get_supabase_url, get_supabase_key

# Output directory (relative to repo root; Streamlit Cloud uses CWD)
_OUTPUTS = Path(os.getenv("OUTPUTS_DIR", "outputs"))


# ---------------------------------------------------------------------------
# Supabase client (cached per session)
# ---------------------------------------------------------------------------
@st.cache_resource(show_spinner=False)
def _get_client():
    url = get_supabase_url()
    key = get_supabase_key()
    if not url or not key:
        return None
    try:
        from supabase import create_client
        return create_client(url, key)
    except Exception:
        return None


def _sb():
    return _get_client()


def _fetch(table: str, filters: dict | None = None,
           order_by: str | None = None, limit: int | None = None) -> list[dict]:
    client = _sb()
    if client is None:
        return []
    try:
        q = client.table(table).select("*")
        for col, val in (filters or {}).items():
            q = q.eq(col, val)
        if order_by:
            desc = order_by.startswith("-")
            q = q.order(order_by.lstrip("-"), desc=desc)
        if limit:
            q = q.limit(limit)
        return q.execute().data or []
    except Exception:
        return []


def _read_json(filename: str) -> list[dict]:
    fpath = _OUTPUTS / filename
    if not fpath.exists():
        return []
    try:
        data = json.loads(fpath.read_text())
        return data if isinstance(data, list) else []
    except Exception:
        return []


# ---------------------------------------------------------------------------
# 1. Signals — today's predictions
# ---------------------------------------------------------------------------
@st.cache_data(ttl=300, show_spinner=False)
def load_signals() -> pd.DataFrame:
    """Latest predictions (today or most recent signal date)."""
    rows = _fetch("predictions", order_by="-signal_date", limit=200)
    if not rows:
        # CSV fallback
        csv = _OUTPUTS / "signals_latest.csv"
        if csv.exists():
            try:
                return pd.read_csv(csv)
            except Exception:
                pass
        # JSON fallback
        all_preds = _read_json("predictions_journal.json")
        if all_preds:
            df = pd.DataFrame(all_preds)
            if "signal_date" in df.columns:
                latest = df["signal_date"].max()
                return df[df["signal_date"] == latest].reset_index(drop=True)
        return pd.DataFrame(columns=["ticker","signal","prob_up","entry_price",
                                      "stop_loss","target_price","risk_reward",
                                      "atr14","horizon_days","signal_date"])

    df = pd.DataFrame(rows)
    if "signal_date" in df.columns:
        latest = df["signal_date"].max()
        df = df[df["signal_date"] == latest]
    return df.reset_index(drop=True)


# ---------------------------------------------------------------------------
# 2. Paper trades
# ---------------------------------------------------------------------------
@st.cache_data(ttl=60, show_spinner=False)
def load_paper_trades() -> pd.DataFrame:
    """All paper trades (open + closed)."""
    rows = _fetch("paper_trades", order_by="-entry_date")
    if rows:
        return pd.DataFrame(rows)

    # portfolio.json fallback
    pf = _OUTPUTS / "portfolio.json"
    if pf.exists():
        try:
            data = json.loads(pf.read_text())
            trades = data.get("trades", [])
            if trades:
                df = pd.DataFrame(trades)
                df["run_id"] = ""
                return df
        except Exception:
            pass
    return pd.DataFrame(columns=["trade_id","ticker","entry_date","entry_price",
                                   "shares","signal","stop_loss","target_price",
                                   "horizon_days","prob_up","status","exit_date",
                                   "exit_price","exit_reason","pnl","pnl_pct"])


@st.cache_data(ttl=60, show_spinner=False)
def load_portfolio_meta() -> dict:
    """Portfolio-level metadata (cash, initial_capital)."""
    pf = _OUTPUTS / "portfolio.json"
    if pf.exists():
        try:
            return json.loads(pf.read_text())
        except Exception:
            pass
    return {"initial_capital": 1_000_000, "cash": 1_000_000}


# ---------------------------------------------------------------------------
# 3. Model runs
# ---------------------------------------------------------------------------
@st.cache_data(ttl=300, show_spinner=False)
def load_model_runs(limit: int = 20) -> pd.DataFrame:
    rows = _fetch("model_runs", order_by="-run_date", limit=limit)
    if rows:
        return pd.DataFrame(rows)

    local = _read_json("model_runs.json")
    if local:
        df = pd.DataFrame(local)
        return df.sort_values("run_date", ascending=False).head(limit).reset_index(drop=True)
    return pd.DataFrame(columns=["run_id","model_version","run_date","oof_ic",
                                   "bt_sharpe","bt_cagr","bt_max_drawdown",
                                   "is_deployed","n_trials"])



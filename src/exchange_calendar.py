_HOLIDAY_TS: pd.DatetimeIndex | None = None


def nse_holidays() -> pd.DatetimeIndex:
    global _HOLIDAY_TS
    if _HOLIDAY_TS is None:
        _HOLIDAY_TS = pd.to_datetime(HOLIDAYS)
    return _HOLIDAY_TS


def trading_days(start: str, end: str) -> pd.DatetimeIndex:
    """Business days between start/end excluding NSE holidays."""
    return pd.bdate_range(start, end, freq="C", holidays=nse_holidays())


def is_trading_day(date: str | pd.Timestamp) -> bool:
    d = pd.Timestamp(date)
    return d in trading_days(str(d.date()), str(d.date()))


def next_trading_day(date: str | pd.Timestamp) -> pd.Timestamp:
    d = pd.Timestamp(date) + pd.Timedelta(days=1)
    tdays = trading_days(str(d.date()), str((d + pd.Timedelta(days=10)).date()))
    return tdays[0] if len(tdays) else d
